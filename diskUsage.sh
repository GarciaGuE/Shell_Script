#!/bin/bash
# Use df to display disk usage information and apply sed to format the output
df | sed -r -n -e '2,$s/^([^ ]+)[ ]+([^ ]+)[ ]+([^ ]+)[ ]+([^ ]+)[ ]+([^ ]+)[ ]+([^ ]+)$/Block file \1, mounted on \6, has \3 blocks used out of a total of \2 (percentage of \5)./p'

echo "-------------------------------------------------------------------------------------------------------------------------------------------------------"
echo "-------------------------------------------------------------------------------------------------------------------------------------------------------"

# Use df and grep to highlight specific lines in the output
df | grep --color -E '^[a-Z0-9/\:]+ +[0-9]+ +[0-9]+ +[0-9]+ +[0-9]+% +[0-9a-Z/]+$'

echo "-------------------------------------------------------------------------------------------------------------------------------------------------------"
echo "-------------------------------------------------------------------------------------------------------------------------------------------------------"

# Use df, sed, and regular expressions to format and display disk usage information
df | sed -r -n -e '2,$s/(^[a-Z0-9/\:]+) +([0-9]+) +([0-9]+) +([0-9]+) +([0-9]+%) +([0-9a-Z/]+)$/Block file \1, mounted on \6, has \3 blocks used out of a total of \2 (percentage of \5)./p'